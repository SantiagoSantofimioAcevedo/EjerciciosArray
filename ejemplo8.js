// Crear un array con números
let grupoNumeros = [5, 12, 8, 25, 3, 17, 10];

// Encontrar el valor máximo y mínimo del array
let maximo = Math.max(...grupoNumeros);
let minimo = Math.min(...grupoNumeros);

// Imprimir el valor máximo y mínimo
console.log("Valor máximo del array:", maximo);
console.log("Valor mínimo del array:", minimo);